#!/usr/bin/node
class Rectangle {
  constructor (w, h) {
    if (w > 0 && h > 0) {
      this.width = w;
      this.height = h;
    } else {
      return 'Rectangle {}';
    }
  }
}
print() instanceof Rectangle; {
  const row = width;
  const column = height;
  for (let i = 0; i < row; i++) {
    for (let j = 0; j < column; j++) {
      console.log('X');
    }
  }
}

module.exports = Rectangle;
